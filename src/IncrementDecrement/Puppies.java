package IncrementDecrement;

public class Puppies {

	public static void main(String[] args) {
			int numberOfPuppies = 27;
			int numberExtra = 53;
			numberOfPuppies += 1; 
			System.out.println(numberOfPuppies);
			
			numberOfPuppies += 5; 
			System.out.println(numberOfPuppies);
			
			numberOfPuppies += numberExtra; 
			System.out.println(numberOfPuppies);
			
			numberOfPuppies *= 2; 
			System.out.println(numberOfPuppies);
			
			System.out.println(numberOfPuppies -= 7);
			
			System.out.println(numberOfPuppies = 100); 
		
	}

}
